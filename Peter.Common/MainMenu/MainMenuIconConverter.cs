            return Brushes.Black;
         }
         return GetIconBrushForFile (fileInfo);
      }

      /// <summary>
      /// Gets a main menu icon brush for the given file.
      /// </summary>
      /// <param name="file">File to get icon brush for.</param>
      /// <returns>The brush for the given file.</returns>
      public static Brush GetIconBrushForFile (FileInfo file)
      {
         if (file == null)
            return Brushes.Black;

         if (Directory.Exists (file.FullName))
            return Brushes.BurlyWood;
         switch (file.Name.ToLower ()) {
            case "gemfile":
            case "rakefile":
               return Brushes.Crimson;
            case "install.exe":
               return Brushes.LightSlateGray;
         }

         switch (file.Extension.ToLower ()) {
            case ".cs":
               return Brushes.ForestGreen;
            case ".js":
            case ".jsx":
            case ".coffee":
               return Brushes.DodgerBlue;
            case ".c":
            case ".cpp":
            case ".cxx":
            case ".h":
            case ".hxx":
               return Brushes.BlueViolet;
            case ".php":
            case ".rar":
               return Brushes.DarkSlateBlue;
            case ".zip":
            case ".7z":
            case ".tar":
               return Brushes.DarkGoldenrod;
            case ".pdf":
            case ".rb":
            case ".ru":
               return Brushes.Crimson;
            case ".bat":
            case ".cmd":
            case ".dos":
               return Brushes.LightSlateGray;
            default:
               return Brushes.Black;
         }
      }
