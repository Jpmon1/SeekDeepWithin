      public static MainMenuIcon GetIconForFile (string file)
      {
         if (string.IsNullOrEmpty (file))
            return MainMenuIcon.File;

         if (Directory.Exists (file))
            return MainMenuIcon.FolderClose;

         FileInfo fileInfo;
         try
         {
            fileInfo = new FileInfo (file);
         }
         catch
         {
            return MainMenuIcon.File;
         }
         return GetIconForFile (fileInfo);
      }

